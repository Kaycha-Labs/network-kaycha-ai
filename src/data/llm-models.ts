/** LLM model inventory — synced across Jericho, Sentinel, Iron-Patriot, Ironman */

export interface LLMModel {
  name: string
  tag: string
  type: 'standard' | 'custom'
  /** Base model for custom models */
  basedOn?: string
  size: string
  /** VRAM required to load (approx) */
  vram: string
  params: string
  quantization: string
  architecture: string
  /** Primary use case */
  purpose: string
  /** Detailed description */
  description: string
  /** Tokens/sec on RTX PRO 6000 96GB (Jericho) — approximate */
  perfJericho?: string
  /** Tokens/sec on RTX 5090 32GB (Sentinel) — approximate */
  perfSentinel?: string
  /** Context window */
  context: string
  /** Which workstations have this model */
  hosts: ('jericho' | 'sentinel' | 'iron-patriot' | 'ironman')[]
}

export interface Workstation {
  id: string
  name: string
  ip: string
  gpu: string
  vramTotal: string
  role: string
  colorKey: string
}

